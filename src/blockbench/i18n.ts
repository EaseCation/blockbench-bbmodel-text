export function registerBBTextTranslations(): void {
  if (typeof Language === 'undefined') return;
  for (const language in BB_TEXT_TRANSLATIONS) {
    Language.addTranslations(language, BB_TEXT_TRANSLATIONS[language]);
  }
}

export function t(key: string, variables?: string | number | (string | number)[], fallback?: string): string {
  if (typeof tl === 'function') return tl(key, variables, fallback);
  return fallback || EN[key] || key;
}
